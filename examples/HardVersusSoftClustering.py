
"""HARD CLUSTERING"""
"""
Hard clustering describes a family of algorithms where each example in a dataset
is assigned to exactly one cluster, as in the k-means and k-means++ algorithms.
"""

"""SOFT OR FUZZY CLUSTERING"""
"""
Algorithms for soft clustering (sometimes also called fuzzy clustering) assign an
example to one or more clusters. A popular example of soft clustering is the fuzzy
C-means (FCM) algorithm (also called soft k-means or fuzzy k-means).
"""
